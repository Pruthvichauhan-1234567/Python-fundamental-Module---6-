# 30. Write a Python program to demonstrate string slicing.

text = "PruthviChauhan"

print("Original String:", text)
print("Slice [0:5]:", text[0:5])
print("Slice [:6]:", text[:6])
print("Slice [6:]:", text[6:])
print("Slice [:]:", text[:])
print("Slice [::2]:", text[::2])
print("Reversed String [::-1]:", text[::-1])
