# 6. How to create variables in Python?
age = 25
print(f"Age (int): {age} ")

height = 18.25
print(f"Height (float): {height}")

name = "Pruthvi"
print(f"Name (string): {name}")

is_student=True
print(f"Is Student (Boolean) : {is_student}")

coordinates=("10.2","50.2")
print(f"Coordinates (Tuple) : {coordinates}")

person={
    "name": "Pruthvi",
    "Age": 19,
    "is_student": "Yes"
}
print(f"person (Dictionary): {person}")

unique_number={1,2,3,2,1}
print(f"Unique Number (set): {unique_number}")

middle_name=None
print("Middle Name (None Type): {middle_name}")